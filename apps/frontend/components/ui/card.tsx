import * as React from 'react'

import { cn } from '@/lib/utils'

/** The panel recipe used across the app: glass surface, hairline border. */
const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement>
>(({ className, ...props }, ref) => (
  <div
    ref={ref}
    className={cn(
      'border-gray/20 rounded-2xl border bg-white/5 backdrop-blur-md',
      className,
    )}
    {...props}
  />
))
Card.displayName = 'Card'

